package Package1;

public class Player {

   private String name;
   private int choice;

    public Player(String name)
    {
        this.name = name;

    }

    public String getName()
    {
        return name;
    }

    public void setName(String name)
    {

        this.name = name;
    }

    public int getChoice()
    {
        return choice;
    }

    public void setChoice(int choice)
    {
        this.choice = choice;
    }



}
